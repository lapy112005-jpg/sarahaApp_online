import { Router } from "express";
import { confirmEmail, confirmForgetPassword, confirmLogin,  forgetPassword,  login, loginGmail, resendConfirmEmail,  resetPassword,  signup, signupGmail } from "./auth.service.js";
import joi from "joi"
import { confirm_email_schema, forget_password_schema, login_schema, signup_schema } from "./auth.validition.js";
import { validition } from "../../middleware/validition.middleware.js";
const router = Router();






router.post("/signup",validition(signup_schema) ,  async (req, res, next) => {
  const result = await signup(req.body)
  return res.status(201).json({ message: "Done signup", result });
});
///////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////
router.post("/confirm-email" ,  async (req, res, next) => {
  const result = await confirmEmail(req.body)
  return res.status(200).json({ message: "confirmed"});
});
//////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////
router.post("/resend-confirm-email" ,  async (req, res, next) => {
  const result = await resendConfirmEmail(req.body)
  return res.status(200).json({ message: "sent successfully .... check your email"});
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/forget-password" ,validition(forget_password_schema),async (req, res, next) => {
  const result = await forgetPassword(req.body);
  return res.status(201).json({ message: "Done ", result });
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/confirm-forget-password" ,  async (req, res, next) => {
  const result = await confirmForgetPassword(req.body)
  return res.status(200).json({ result});
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/reset-password" ,  async (req, res, next) => {
  const result = await resetPassword(req.body)
  return res.status(200).json({ result});
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/login",validition(login_schema) ,async (req, res, next) => {
  const result = await login(req.body);
  return res.status(201).json({ message: "Done login", result });
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/confirm-login" ,async (req, res, next) => {
  const result = await confirmLogin(req.body);
  return res.status(201).json({ message: "Done login", result });
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/signup/gmail", async (req, res, next) => {
  const account = await signupGmail(req.body);
  return res.status(201).json({ message: " Done" , account });
});
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
router.post("/login/gmail", async (req, res, next) => {
  const account = await loginGmail(req.body);
  return res.status(201).json({ message: " Done" , account });
});
export default router;

